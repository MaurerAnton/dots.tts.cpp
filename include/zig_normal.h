// Ziggurat normal sampler using MT19937 next() as uniform source
template<typename MT>
inline float zig_normal(MT& mt) {
    for (;;) {
        uint32_t u = mt.next();
        unsigned char idx = u & 255;
        float x = (int)(u >> 24) * ZIG_W[idx];
        if ((int)(u >> 24) < ZIG_K[idx])
            return x;
        if (idx == 0) {
            float u1, u2;
            do {
                u1 = (mt.next() >> 5) * (1.0f / 134217728.0f);
                u2 = (mt.next() >> 5) * (1.0f / 134217728.0f);
                x = -logf(u1 < 1e-6f ? 1e-6f : u1) / ZIG_R;
            } while (u2 * u2 > expf(-0.5f * (x * x - ZIG_R * ZIG_R)));
            return (mt.next() & 1) ? x : -x;
        }
        float uf = (mt.next() & 0x007fffff) * (1.0f / 8388608.0f);
        if (uf < expf(-0.5f * x * x) - ZIG_F[idx])
            return x;
    }
}
